package com.prec.video;


public class VideoAtt {
	int sno;
	int serial;
	String pOriFile;
	String pSysFile;
	String tOriFile;
	String tSysFile;

	
	public VideoAtt() {}

	public int getSno() {
		return sno;
	}

	public void setSno(int sno) {
		this.sno = sno;
	}

	public int getSerial() {
		return serial;
	}

	public void setSerial(int serial) {
		this.serial = serial;
	}

	public String getpOriFile() {
		return pOriFile;
	}

	public void setpOriFile(String pOriFile) {
		this.pOriFile = pOriFile;
	}

	public String getpSysFile() {
		return pSysFile;
	}

	public void setpSysFile(String pSysFile) {
		this.pSysFile = pSysFile;
	}

	public String gettOriFile() {
		return tOriFile;
	}

	public void settOriFile(String tOriFile) {
		this.tOriFile = tOriFile;
	}

	public String gettSysFile() {
		return tSysFile;
	}

	public void settSysFile(String tSysFile) {
		this.tSysFile = tSysFile;
	}


	
	
}
